const { Sequelize } = require('sequelize');
const dotenv = require('dotenv');

dotenv.config();

const sequelize = new Sequelize(
  process.env.DB_NAME || 'chakra_art',
  process.env.DB_USER || 'root',
  process.env.DB_PASSWORD || '',
  {
    host: process.env.DB_HOST || '127.0.0.1',
    port: process.env.DB_PORT || 3306,
    dialect: 'mysql',
    logging: false, // Turn off query logs to keep console clean
    pool: {
      max: 5,
      min: 0,
      acquire: 30000,
      idle: 10000
    }
  }
);

const connectDB = async () => {
  try {
    await sequelize.authenticate();
    console.log('MySQL Connected successfully using Sequelize.');
  } catch (error) {
    console.error(`Database Connection Warning: ${error.message}`);
    console.error('The server remains active, but SQL database features will be offline until MySQL is started.');
  }
};

module.exports = { sequelize, connectDB };
